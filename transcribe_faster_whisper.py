def main() -> None:
	logger.basic("-- Parsing Args --")
	args = parse_args()
	logger.debug(timer.checkpoint("Args parsed"))
	logger.basic("-- Parsed Args --")

	logger.basic("-- Parsing Input File/s --")
	if not args.input_path.exists():
		raise FileNotFoundError(f"Input path not found: {args.input_path}")

	# Determine and filter list of files to process
	def _has_subtitle(video_file: Path) -> bool:
		"""Check if video file has existing subtitle in the target format."""
		return video_file.with_suffix(args.format).exists()

	# Discover video files from input path
	if args.input_path.is_file():
		if args.input_path.suffix.lower() == '.mkv':
			video_files = [args.input_path]
		else:
			raise ValueError(f"Input file must be MKV format, got: {args.input_path.suffix}")
	elif args.input_path.is_dir():
		video_files = sorted(args.input_path.glob("*.mkv"))
		if not video_files:
			logger.basic(f"- No MKV files found in {args.input_path}")
			return
		logger.basic(f"- Found {len(video_files)} MKV files")
	else:
		raise ValueError(f"Input path is neither file nor directory: {args.input_path}")

	# Filter out files with existing subtitles (unless --force-recreate)
	if not args.force_recreate:
		video_files = [
			vf for vf in video_files
			if not (_has_subtitle(vf) and not logger.basic(f"- Subtitle exists for {vf.name}, skipping"))
		]
		
		if not video_files:
			logger.basic("- All files have existing subtitles, nothing to process")
			return
	logger.debug(f"- {len(video_files)} files to process after filtering existing subtitles -")
	logger.debug(timer.checkpoint("Parsed Input File/s"))
	logger.basic("-- Parsed Input File/s --")

	# === DUBTITLE PATH: Use existing subtitle track (bypasses all ML processing) ===
	if args.use_dubtitle:
		logger.basic(f"-- Dubtitle mode: Looking for '{args.use_dubtitle}' subtitle track --")
		
		for video_file in video_files:
			logger.basic(f"\n-- Processing (dubtitle): {video_file.name} --")
			output_path = video_file.with_suffix(args.format)
			
			if process_dubtitle(video_file, output_path, args.use_dubtitle, args.extract_subtitle):
				logger.basic(f"-- Completed (dubtitle): {video_file.name} --")
			else:
				logger.basic(f"-- Skipped (no matching dubtitle): {video_file.name} --")
		
		logger.basic("\n-- Dubtitle processing complete --")
		return  # Exit early, no ML processing needed

	# === TRANSCRIPTION PATH: Load models and process with Whisper ===
	logger.basic("-- Loading Whisper Model --")
	model = WhisperModel(args.model, device="cuda", compute_type=args.compute_type)
	logger.debug(timer.checkpoint("Whisper model loaded"))
	logger.basic("-- Whisper Model loaded --")

	if args.diarise:
		logger.basic("-- Importing modules for Diarization --")

		global Pipeline
		global ProgressHook
		global torch
		global torchaudio

		from pyannote.audio import Pipeline
		logger.debug(timer.checkpoint("Pipeline loaded"))
		from pyannote.audio.pipelines.utils.hook import ProgressHook
		logger.debug(timer.checkpoint("ProgressHook loaded"))
		# Suppress pyannote warnings about TF32 and std() edge cases
		warnings.filterwarnings("ignore", category=UserWarning, module="pyannote.audio")
		import torch
		logger.debug(timer.checkpoint("Torch loaded"))
		# Enable TF32 globally before any operations
		torch.backends.cuda.matmul.allow_tf32 = True
		torch.backends.cudnn.allow_tf32 = True
		torch.set_float32_matmul_precision('high')
		import torchaudio
		logger.debug(timer.checkpoint("TorchAudio loaded"))

		logger.debug(timer.checkpoint("Modules for Diarization loaded"))
		logger.basic("-- Completed Importing modules for Diarization --")
		logger.basic("-- Loading Model for Diarization --")

		hf_token = _config.get("tokens", "hf_read_token", fallback=None)
		diar_pipeline = Pipeline.from_pretrained(
			"pyannote/speaker-diarization-community-1",
			token=hf_token
		)
		# Run diarization on GPU for higher accuracy.
		diar_pipeline.to(torch.device("cuda"))
		
		# Adjust onset threshold from config to reduce false positives on non-speech audio
		# Default is 0.5; higher values require stronger speech signal
		onset_threshold = _config.getfloat("detection", "onset", fallback=0.5)
		try:
			diar_pipeline._segmentation.onset = onset_threshold
			logger.debug(f"Pyannote onset threshold set to {onset_threshold}")
		except AttributeError:
			logger.debug("Could not adjust pyannote onset threshold (API may have changed)")
		
		logger.debug(timer.checkpoint("Diarization model loaded"))
		logger.basic("-- Completed Loading Model for Diarization --")
	else:
		diar_pipeline = None
	
	# Fetch context once for all files
	initial_context = _get_context(args)

	# Initialize video handler variable
	current_video_handler = None

	# Process each video file
	for video_file in video_files:
		# Setup per-video logging if enabled
		if current_video_handler:
			remove_handler_from_logger(logger, current_video_handler)
		
		if general_handler:
			try:
				video_sanitized = sanitize_filename(video_file.name)
				video_log_path = log_dir / f"{video_sanitized}.log"
				current_video_handler = logging.FileHandler(video_log_path, encoding='utf-8')
				formatter = logging.Formatter('%(asctime)s - %(levelname)s - %(message)s')
				current_video_handler.setFormatter(formatter)
				current_video_handler.setLevel(logging.DEBUG)
				add_handler_to_logger(logger, current_video_handler)
			except Exception as e:
				logger.debug(f"Error setting up video logging: {e}")
				current_video_handler = None
		
		logger.basic(f"\n-- Processing: {video_file.name} --")
		
		# Determine output path based on format
		output_path = video_file.with_suffix(args.format)
		
		# Ensure tmp directory exists
		tmp_dir = Path("/mnt/i/python/tmp")
		tmp_dir.mkdir(parents=True, exist_ok=True)
		
		# Extract audio from video
		with tempfile.NamedTemporaryFile(suffix=".wav", delete=False, dir=str(tmp_dir)) as tmp_audio:
			audio_path = Path(tmp_audio.name)
		
		try:
			if not _extract_audio_from_video(video_file, audio_path):
				logger.basic(f"- Failed to extract audio from {video_file.name}, skipping")
				continue
			
			logger.debug(timer.checkpoint("Audio extracted"))
			
			# Transcribe the extracted audio
			transcribe_to_subtitles(
				audio_path=audio_path,
				video_path=video_file,
				model=model,
				beam_size=args.beam_size,
				vad_filter=args.vad_filter,
				word_timestamps=args.word_timestamps,
				words_path=args.words_json,
				output_path=output_path,
				diarise=args.diarise,
				vocals=args.vocals,
				initial_context=initial_context,
				diar_pipeline=diar_pipeline,
				extract_subtitle=args.extract_subtitle,
				subtitle_lead_ms=args.subtitle_lead_ms,
				subtitle_lag_ms=args.subtitle_lag_ms,
				debug_audio_dir=tmp_dir if args.no_cleanup else None,
			)
			logger.debug(timer.checkpoint(f"Transcription complete for {video_file.name}"))
			logger.basic(f"-- Completed processing: {video_file.name} --")
		
		finally:
			# Clean up extracted audio and vocals file (unless --no-cleanup)
			if not args.no_cleanup:
				if audio_path.exists():
					audio_path.unlink()
					logger.debug(f"- Cleaned up temporary audio: {audio_path}")
				
				# Clean up vocals file if it exists (created by isolate_vocals)
				vocals_path = audio_path.with_name(audio_path.stem + "_vocals.wav")
				if vocals_path.exists():
					vocals_path.unlink()
					logger.debug(f"- Cleaned up vocals audio: {vocals_path}")
			else:
				logger.debug(f"- Keeping temporary files (--no-cleanup): {audio_path}")
	
	# Clean up video handler if it exists
	if current_video_handler:
		remove_handler_from_logger(logger, current_video_handler)

if __name__ == "__main__":
	main()
