def transcribe_with_diarization(
	audio_path: Path,
	model: WhisperModel,
	beam_size: int,
	vad_filter: bool,
	word_timestamps: bool,
	diar_pipeline,
	initial_context: str = "",
	debug_audio_dir: Path | None = None,
) -> list[TranscribedSegment]:
	"""Transcribe audio using pyannote diarization segments with silence trimming.
	
	Args:
		debug_audio_dir: If set, save processed audio chunks to this directory for debugging.
	"""

	logger.basic("-- Running Diarization --")
	logger.debug(_print_gpu_memory())
	
	diarization = diarize_audio(str(audio_path), diar_pipeline)
	logger.debug(timer.checkpoint("Diarization complete"))
	logger.basic("-- Diarization Complete --")
	logger.debug(_print_gpu_memory())
	
	# Free diarization GPU memory
	gc.collect()
	torch.cuda.empty_cache()
	logger.basic("-- Freed GPU Memory --")
	logger.debug(_print_gpu_memory())
	
	logger.basic("-- Load Audio / resample --")
	# Load audio once
	waveform, sr = torchaudio.load(str(audio_path))
	resample = torchaudio.transforms.Resample(sr, 16000)

	out_segments = []
	logger.debug(timer.checkpoint("Audio loaded"))
	logger.basic("-- Load Audio / resample complete --")

	# Collect all turns first to detect overlaps
	turns_list = list(diarization.speaker_diarization)
	
	logger.basic("-- Starting transcription of diarization segments --")
	for idx, (turn, speaker) in enumerate(turns_list, start=1):
		# Skip very short segments (< 0.2s) - prone to hallucination
		segment_duration = turn.end - turn.start
		if segment_duration < 0.2:
			logger.debug(f"Skipping segment {idx} (too short: {segment_duration:.2f}s)")
			continue

		# Extract full chunk from diarization boundaries
		start_sample = int(turn.start * sr)
		end_sample = int(turn.end * sr)
		chunk = waveform[:, start_sample:end_sample]
		if chunk.shape[0] > 1:
			chunk = chunk.mean(dim=0, keepdim=True)
		chunk_16k = resample(chunk).squeeze().to(torch.float32).numpy()

		# Apply loudness normalization if enabled
		loudnorm_enabled = _config.getboolean("detection", "loudnorm", fallback=True)
		if loudnorm_enabled:
			logger.basic(f"- performing loudness normalization against segment {idx} -")
			chunk_16k = _normalize_audio(chunk_16k, sr=16000)

		# Detect and trim leading/trailing silence
		silence_start, silence_end = _detect_silence_boundaries(chunk_16k, sr=16000)
		trim_offset = silence_start / 16000  # Convert to seconds
		trimmed_chunk = chunk_16k[silence_start:silence_end]
		
		# Adjust effective start to account for trimmed silence
		effective_start = turn.start + trim_offset

		# Save debug audio chunk if requested (saves what Silero will analyze)
		if debug_audio_dir:
			import numpy as np
			import scipy.io.wavfile as wav
			# Build filename: {audio_stem}[_loudnorm]_segment{idx}.wav
			loudnorm_suffix = "_loudnorm" if loudnorm_enabled else ""
			debug_filename = f"{audio_path.stem}{loudnorm_suffix}_segment{idx:03d}.wav"
			debug_path = debug_audio_dir / debug_filename
			# Convert float32 [-1, 1] to int16 for WAV
			audio_int16 = (trimmed_chunk * 32767).astype(np.int16)
			wav.write(str(debug_path), 16000, audio_int16)
			logger.debug(f"Saved debug audio: {debug_path.name}")

		# Pre-check with Silero VAD before transcription (controlled by --vad-filter arg)
		if vad_filter:
			# Pad audio with silence to help VAD detect onset (speech starting at 0.0s confuses VAD)
			import numpy as np
			silero_padding_ms = _config.getfloat("detection", "silero_padding_ms", fallback=500.0)
			padding_samples = int(silero_padding_ms / 1000.0 * 16000)
			padded_chunk = np.concatenate([
				np.zeros(padding_samples, dtype=np.float32),  # Silence before
				trimmed_chunk,
				np.zeros(padding_samples, dtype=np.float32),  # Silence after
			])
			
			silero_threshold = _config.getfloat("detection", "silero_vad_threshold", fallback=0.5)
			has_speech, speech_prob = _check_speech_with_silero(padded_chunk, sr=16000, threshold=silero_threshold)
			
			if not has_speech:
				logger.debug(f"Skipping segment {idx} (Silero VAD detected no speech, ratio={speech_prob:.3f})")
				continue
		else:
			# No VAD pre-check, set default values for downstream filters
			has_speech = True
			speech_prob = 1.0

		# Format timestamp as HH:MM:SS for easy video seeking
		ts_hours, ts_rem = divmod(int(turn.start), 3600)
		ts_mins, ts_secs = divmod(ts_rem, 60)
		ts_str = f"{ts_hours:02}:{ts_mins:02}:{ts_secs:02}"
		
		logger.debug(f"Processing segment {idx} @ {ts_str}...")
		logger.debug(f"Original: {turn.start:.3f}s-{turn.end:.3f}s | Trimmed: {trim_offset:.3f}s silence removed")
		logger.debug(f"Silero VAD: has_speech={has_speech}, speech_ratio={speech_prob:.3f}")
		logger.debug(_print_gpu_memory())
		
		transcribe_params = {
			"beam_size": beam_size,
			"language": "en",
			"word_timestamps": word_timestamps,
			"condition_on_previous_text": True,
			"no_speech_threshold": 0.6,
			"compression_ratio_threshold": 2.4,
			"hallucination_silence_threshold": 0.5,
			"temperature": [0.0, 0.1],
			"repetition_penalty": 1,
			"best_of": 5,
			"patience": 1.2,
		}
		
		if initial_context:
			transcribe_params["initial_prompt"] = initial_context
		
		seg_iter, _ = model.transcribe(trimmed_chunk, **transcribe_params)
			
		for seg in seg_iter:
			text = seg.text.strip()
			
			# Log confidence metrics to help diagnose hallucinations
			no_speech_prob = getattr(seg, 'no_speech_prob', None)
			avg_logprob = getattr(seg, 'avg_logprob', None)
			nsp_str = f"{no_speech_prob:.3f}" if no_speech_prob is not None else "N/A"
			alp_str = f"{avg_logprob:.3f}" if avg_logprob is not None else "N/A"
			logger.debug(f"Segment text: '{text}' | no_speech_prob: {nsp_str} | avg_logprob: {alp_str}")
			
			# Hallucination filter: low speech_ratio combined with low avg_logprob
			# Configurable thresholds from config file
			filter_speech_ratio = _config.getfloat("detection", "filter_speech_ratio", fallback=0.700)
			filter_avg_logprob = _config.getfloat("detection", "filter_avg_logprob", fallback=-0.500)
			
			if (speech_prob < filter_speech_ratio and 
				avg_logprob is not None and avg_logprob < filter_avg_logprob):
				logger.debug(f"Skipping potential hallucination (speech_ratio={speech_prob:.3f} < {filter_speech_ratio} AND avg_logprob={avg_logprob:.3f} < {filter_avg_logprob}): '{text}'")
				continue
			
			# Rule 2: Ultra-low confidence filter - very uncertain transcription with non-speech detected
			filter_ultra_low_logprob = _config.getfloat("detection", "filter_ultra_low_logprob", fallback=-1.0)
			filter_no_speech_prob = _config.getfloat("detection", "filter_no_speech_prob", fallback=0.010)
			
			if (avg_logprob is not None and avg_logprob < filter_ultra_low_logprob and
				no_speech_prob is not None and no_speech_prob > filter_no_speech_prob):
				logger.debug(f"Skipping ultra-low confidence (avg_logprob={avg_logprob:.3f} < {filter_ultra_low_logprob} AND no_speech_prob={no_speech_prob:.3f} > {filter_no_speech_prob}): '{text}'")
				continue
			
			# Skip empty or suspiciously short text
			if not text or len(text) < 2:
				continue
			
			# Limit excessive character repetition (e.g., "HEEEEE..." to max 10 repeated chars)
			def limit_repetition(text: str, max_repeat: int = 10) -> str:
				"""Limit consecutive character repetition to max_repeat."""
				return re.sub(r'(.)\1{' + str(max_repeat) + r',}', lambda m: m.group(1) * max_repeat, text)
			
			text = limit_repetition(text)
			
			# Skip common hallucination phrases
			hallucination_phrases = [
				"thank you for watching",
				"thanks for watching",
				"subscribe",
				"like and subscribe",
				"Subtitles by the Amara.org community",
				"see you next time",
			]
			if any(phrase in text.lower() for phrase in hallucination_phrases):
				logger.debug(f"Skipping hallucination phrase: {text}")
				continue
			
			# Skip text that is only ellipsis or punctuation
			if text.strip("., !?") == "":
				logger.debug(f"Skipping punctuation-only: {text}")
				continue

			# Adjust timestamps: relative to trimmed chunk + offset from original boundary
			final_start = effective_start + seg.start
			final_end = effective_start + seg.end
			logger.debug(f"Start: {final_start:.3f}s, End: {final_end:.3f}s, Text: {text}")
			out_segments.append(TranscribedSegment(
				start=final_start,
				end=final_end,
				text=text,
				speaker=None,
			))

		logger.debug(f"Segment {idx} complete")
		torch.cuda.empty_cache()

	# Sort by start time
	out_segments.sort(key=lambda s: s.start)
	
	# Merge overlapping segments for proper subtitle display
	out_segments = _merge_overlapping_segments(out_segments)
	logger.debug(timer.checkpoint("Diarization transcription complete"))
	return out_segments

def transcribe_to_subtitles(
	audio_path: Path,
	video_path: Path,
	model: WhisperModel,
	beam_size: int,
	vad_filter: bool,
	word_timestamps: bool,
	words_path: Path | None,
	output_path: Path,
	diarise: bool,
	vocals: bool,
	initial_context: str = "",
	diar_pipeline=None,
	extract_subtitle: str | None = None,
	subtitle_lead_ms: float = 400.0,
	subtitle_lag_ms: float = 600.0,
	debug_audio_dir: Path | None = None,
) -> Path:
	"""Main transcription orchestrator that outputs to SRT or ASS format.
	
	Format is determined by output_path extension (.srt or .ass).
	Defaults to ASS if no recognized extension.
	Optionally extracts and merges subtitle tracks from the MKV.
	
	Args:
		subtitle_lead_ms: Start text this many ms before audio starts (default: 400ms)
		subtitle_lag_ms: Keep text this many ms after audio ends (default: 600ms)
	"""
	# Determine output format from file extension
	output_format = output_path.suffix.lower()

	# Isolate vocals FIRST so both diarization and transcription work on cleaner audio
	if vocals:
		logger.basic("-- Isolating vocals with audio-separator --")
		audio_path = isolate_vocals(audio_path)
		logger.debug(timer.checkpoint("Vocals isolated"))

	logger.debug(f"Current Audio file: {audio_path}")

	if diarise:
		# Diarization path ignores word_timestamps
		segments = transcribe_with_diarization(audio_path, model, beam_size, vad_filter, word_timestamps, diar_pipeline, initial_context, debug_audio_dir)
	else:
		logger.basic("-- Transcribing Full Audio --")
		segments = transcribe_full_audio(
			audio_path, model, beam_size, vad_filter, word_timestamps, words_path
		)

	# Apply timing adjustments (convert ms to seconds)
	# Config values used as defaults, CLI args can override
	config_lead = _config.getfloat("transcription", "subtitle_before_padding", fallback=400.0)
	config_lag = _config.getfloat("transcription", "subtitle_after_padding", fallback=600.0)
	
	# Use CLI args if provided, otherwise fall back to config
	lead_offset = (subtitle_lead_ms if subtitle_lead_ms != 400.0 else config_lead) / 1000.0
	lag_offset = (subtitle_lag_ms if subtitle_lag_ms != 600.0 else config_lag) / 1000.0
	
	for seg in segments:
		seg.start = max(0, seg.start - lead_offset)  # Start earlier
		seg.end = seg.end + lag_offset  # End later

	# Write in appropriate format
	if output_format == ".srt":
		_write_srt(segments, output_path)
		logger.basic(f"Wrote SRT: {output_path}")
	else:  # ASS format
		mkv_title = _get_mkv_title(video_path)
		mkv_resolution = _get_mkv_resolution(video_path)
		_write_ass(segments, output_path, mkv_title, mkv_resolution)
		logger.basic(f"Wrote ASS: {output_path}")
		
		# Extract and merge external subtitle if requested
		if extract_subtitle:
			extracted_path = _extract_subtitle_from_mkv(video_path, extract_subtitle)
			if extracted_path:
				# Convert to styled ASS if not already ASS
				if extracted_path.suffix.lower() != ".ass":
					ass_temp = extracted_path.parent / f"{extracted_path.stem}_signs.ass"
					_convert_secondary_to_signs_ass(extracted_path, ass_temp, mkv_resolution)
					extracted_path.unlink()
					extracted_path = ass_temp
				
				# Merge with transcription
				merged_path = output_path.parent / f"{output_path.stem}_merged.ass"
				_merge_ass_subtitles(output_path, extracted_path, merged_path)
				output_path.unlink()
				merged_path.rename(output_path)
				extracted_path.unlink()
				logger.basic(f"Merged '{extract_subtitle}' subtitles into {output_path.name}")
	
	return output_path


def _convert_subtitle_to_ass(input_path: Path, output_path: Path) -> None:
	"""Convert subtitle file to ASS format using ffmpeg.
	
	Args:
		input_path: Path to input subtitle file (SRT, SSA, etc)
		output_path: Path to output ASS file
	"""
	try:
		subprocess.run(
			["ffmpeg", "-y", "-i", str(input_path), str(output_path)],
			capture_output=True,
			timeout=30,
			check=True
		)
		logger.debug(f"Converted {input_path.name} to ASS format")
	except Exception as e:
		logger.debug(f"Error converting to ASS: {e}")
		raise


def _convert_secondary_to_signs_ass(input_path: Path, output_path: Path, mkv_resolution: tuple[int, int]) -> None:
	"""Convert secondary subtitle (Signs/Songs) to ASS with Signs style.
	
	Used when secondary subtitle is SRT or other non-ASS format.
	Applies top-center positioning with subtle aqua color.
	"""
	play_res_x, play_res_y = mkv_resolution
	signs_font_size = int(44 * play_res_y / 1080)  # Signs: 44pt at 1080p (slightly larger)
	
	# Convert to ASS first via ffmpeg
	temp_ass = input_path.parent / f"{input_path.stem}_temp_convert.ass"
	_convert_subtitle_to_ass(input_path, temp_ass)
	
	# Parse dialogues
	_, dialogues = _parse_ass_file(temp_ass)
	temp_ass.unlink()
	
	# Build header with Signs style
	ass_header = f"""[Script Info]
; Secondary subtitle converted by transcribe_faster_whisper.py
ScriptType: v4.00+
PlayResX: {play_res_x}
PlayResY: {play_res_y}

[V4+ Styles]
Format: Name, Fontname, Fontsize, PrimaryColour, SecondaryColour, OutlineColour, BackColour, Bold, Italic, Underline, StrikeOut, ScaleX, ScaleY, Spacing, Angle, BorderStyle, Outline, Shadow, Alignment, MarginL, MarginR, MarginV, Encoding
Style: Signs,Arial,{signs_font_size},&H00FFFFC8,&H000000FF,&H64000000,&H64000000,-1,0,0,0,100,100,0,0,1,2,2,8,10,10,10,1

[Events]
Format: Layer, Start, End, Style, Name, MarginL, MarginR, MarginV, Effect, Text
"""
	
	# Remap all dialogues to Signs style
	restyled_dialogues = []
	for line in dialogues:
		if line.startswith("Dialogue:"):
			parts = line.split(",", 9)
			if len(parts) >= 10:
				parts[3] = "Signs"
				restyled_dialogues.append(",".join(parts))
			else:
				restyled_dialogues.append(line)
		else:
			restyled_dialogues.append(line)
	
	with output_path.open("w", encoding="utf-8") as fh:
		fh.write(ass_header)
		fh.writelines(restyled_dialogues)
	
	logger.debug(f"Converted {input_path.name} to Signs-styled ASS: {len(restyled_dialogues)} dialogues")


def _convert_subtitle_to_styled_ass(input_path: Path, output_path: Path, mkv_title: str, mkv_resolution: tuple[int, int]) -> None:
	"""Convert subtitle to ASS with proper header styling (matching transcription output).
	
	Extracts dialogues from input subtitle and writes them with our standard ASS header.
	"""
	play_res_x, play_res_y = mkv_resolution
	font_size = int(38 * play_res_y / 1080)  # ClosedCaptions: 38pt at 1080p
	signs_font_size = int(44 * play_res_y / 1080)  # Signs: 44pt at 1080p (slightly larger)
	
	# If already ASS, parse and restyle; otherwise convert first
	if input_path.suffix.lower() == '.ass':
		temp_ass = input_path
	else:
		# Convert to ASS first via ffmpeg
		temp_ass = input_path.parent / f"{input_path.stem}_temp_convert.ass"
		_convert_subtitle_to_ass(input_path, temp_ass)
	
	# Parse dialogues from the ASS file
	_, dialogues = _parse_ass_file(temp_ass)
	
	# Clean up temp file if we created it
	if temp_ass != input_path and temp_ass.exists():
		temp_ass.unlink()
	
	# Build our standard ASS header
	ass_header = f"""[Script Info]
; Script generated by transcribe_faster_whisper.py
Title: {mkv_title}
ScriptType: v4.00+
Collisions: Normal
PlayResX: {play_res_x}
PlayResY: {play_res_y}
Timer: 100.0000

[V4+ Styles]
Format: Name, Fontname, Fontsize, PrimaryColour, SecondaryColour, OutlineColour, BackColour, Bold, Italic, Underline, StrikeOut, ScaleX, ScaleY, Spacing, Angle, BorderStyle, Outline, Shadow, Alignment, MarginL, MarginR, MarginV, Encoding
Style: Default,Arial,{font_size},&H00FFFFFF,&H000000FF,&H64000000,&H64000000,-1,0,0,0,100,100,0,0,1,2,2,2,10,10,10,1
Style: ClosedCaptions,Arial,{font_size},&H00FFFFFF,&H000000FF,&H64000000,&H64000000,-1,0,0,0,100,100,0,0,1,2,2,2,10,10,10,1
Style: Signs,Arial,{signs_font_size},&H00FFFFC8,&H000000FF,&H64000000,&H64000000,-1,0,0,0,100,100,0,0,1,2,2,8,10,10,10,1

[Events]
Format: Layer, Start, End, Style, Name, MarginL, MarginR, MarginV, Effect, Text
"""
	
	# Remap dialogues to use ClosedCaptions style
	restyled_dialogues = []
	for line in dialogues:
		# Parse dialogue: Dialogue: Layer,Start,End,Style,Name,MarginL,MarginR,MarginV,Effect,Text
		if line.startswith("Dialogue:"):
			parts = line.split(",", 9)  # Split into max 10 parts
			if len(parts) >= 10:
				# Replace style (index 3) with ClosedCaptions
				parts[3] = "ClosedCaptions"
				restyled_dialogues.append(",".join(parts))
			else:
				restyled_dialogues.append(line)
		else:
			restyled_dialogues.append(line)
	
	# Write output
	with output_path.open("w", encoding="utf-8") as fh:
		fh.write(ass_header)
		fh.writelines(restyled_dialogues)
	
	logger.debug(f"Converted {input_path.name} to styled ASS: {len(restyled_dialogues)} dialogues")


def process_dubtitle(
	video_path: Path,
	output_path: Path,
	dubtitle_pattern: str,
	extract_subtitle: str | None = None,
) -> bool:
	"""Process video using existing subtitle track instead of transcription.
	
	Extracts the dubtitle track, converts to styled ASS, and optionally merges
	with another subtitle track (e.g., Signs/Songs).
	
	Args:
		video_path: Path to MKV file
		output_path: Path for output ASS file
		dubtitle_pattern: Pattern to match subtitle track name
		extract_subtitle: Optional pattern for secondary subtitle to merge
	
	Returns:
		True if successful, False if dubtitle track not found
	"""
	# Extract the dubtitle track
	dubtitle_path = _extract_subtitle_from_mkv(video_path, dubtitle_pattern)
	if dubtitle_path is None:
		logger.basic(f"-- No subtitle track matching '{dubtitle_pattern}' found in {video_path.name}")
		return False
	
	logger.basic(f"-- Found dubtitle track: {dubtitle_path.name}")
	
	# Get video metadata for ASS header
	mkv_title = _get_mkv_title(video_path)
	mkv_resolution = _get_mkv_resolution(video_path)
	
	# Convert to styled ASS
	_convert_subtitle_to_styled_ass(dubtitle_path, output_path, mkv_title, mkv_resolution)
	dubtitle_path.unlink()  # Clean up extracted file
	
	logger.basic(f"-- Converted dubtitle to ASS: {output_path.name}")
	
	# Optionally merge with secondary subtitle (e.g., Signs/Songs)
	if extract_subtitle:
		extracted_path = _extract_subtitle_from_mkv(video_path, extract_subtitle)
		if extracted_path:
			# Convert to styled ASS if not already ASS
			if extracted_path.suffix.lower() != ".ass":
				ass_temp = extracted_path.parent / f"{extracted_path.stem}_signs.ass"
				_convert_secondary_to_signs_ass(extracted_path, ass_temp, mkv_resolution)
				extracted_path.unlink()
				extracted_path = ass_temp
			
			# Merge with dubtitle
			merged_path = output_path.parent / f"{output_path.stem}_merged.ass"
			_merge_ass_subtitles(output_path, extracted_path, merged_path)
			output_path.unlink()
			merged_path.rename(output_path)
			extracted_path.unlink()
			logger.basic(f"-- Merged '{extract_subtitle}' subtitles into {output_path.name}")
		else:
			logger.basic(f"-- No '{extract_subtitle}' track found to merge")
	
	return True


