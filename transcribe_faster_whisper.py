def diarize_audio(audio_path: str, diar_pipeline):
	"""Run pyannote diarization and return the annotation object.
	
	Preloads audio into memory to speed up the embeddings phase.
	"""
	# Preload audio into memory to avoid repeated disk I/O during embeddings
	logger.debug(f"Preloading audio from {audio_path}...")
	waveform, sample_rate = torchaudio.load(audio_path)
	
	# Construct the dict expected by pyannote (keeps waveform on CPU; 
	# pipeline moves chunks to GPU internally)
	audio_in_memory = {
		"waveform": waveform,
		"sample_rate": sample_rate
	}
	logger.debug(f"Audio preloaded: {waveform.shape}, {sample_rate}Hz")

	with ProgressHook() as hook:
		result = diar_pipeline(audio_in_memory, hook=hook)

	if logger.debug_enabled:
		for idx, (turn, speaker) in enumerate(result.speaker_diarization, start=1):
			duration = turn.end - turn.start
			logger.debug(f"Diarization segment {idx}: {turn.start:.2f}s - {turn.end:.2f}s ({duration:.2f}s) Speaker: {speaker}")
	
	return result


def isolate_vocals(audio_path: Path) -> Path:
	"""Isolate vocals using audio-separator and return path to vocals audio."""
	from audio_separator.separator import Separator

	# Configure audio-separator logging based on debug flag
	if logger.debug_enabled:
		log_level = logging.INFO
	else:
		log_level = logging.WARNING

	# Get model directory from config (defaults to /tmp/audio-separator-models/)
	model_dir = _config.get("audio-separator", "model_dir", fallback=None)

	audio_str = str(audio_path)
	base_name = os.path.splitext(audio_str)[0]
	vocals_path = Path(f"{base_name}_vocals.wav")

	sep = Separator(
		log_level=log_level,
		output_single_stem="Vocals",
		model_file_dir=model_dir,
		sample_rate=16000,
		output_format="WAV",
	)
	sep.load_model(model_filename="vocals_mel_band_roformer.ckpt")
	output_files = sep.separate(audio_str)

	if output_files:
		# Find the file with "Vocals" in the name (case-insensitive)
		vocals_file = None
		for f in output_files:
			if "vocals" in Path(f).name.lower():
				vocals_file = Path(f)
				break
		
		if vocals_file:
			vocals_file.rename(vocals_path)
			logger.basic(f"-- Vocals isolated at: {vocals_path}")
			return vocals_path
		else:
			logger.basic(f"-- Warning: No vocals file found in outputs: {[Path(f).name for f in output_files]}")
			logger.basic("-- Using original audio")

	logger.basic("-- audio-separator returned no outputs; using original audio")
	return Path(audio_str)


