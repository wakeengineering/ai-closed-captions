def get_http_context(anime_title: str, limit: int = 900) -> str:
	"""Fetch anime character names and metadata from AniList API for Whisper context."""
	import requests
	
	url = 'https://graphql.anilist.co'
	query = '''
	query ($search: String) {
	  Media (search: $search, type: ANIME) {
	    title { romaji english }
	    tags { name }
	    characters (sort: ROLE, perPage: 25) {
	      nodes { name { full } }
	    }
	  }
	}
	'''
	
	variables = {'search': anime_title}
	
	try:
		response = requests.post(url, json={'query': query, 'variables': variables})
		data = response.json()['data']['Media']
		
		# Build list from Title, Characters, and Tags
		prompt_parts = []
		prompt_parts.append(data['title']['romaji'])
		if data['title']['english']:
			prompt_parts.append(data['title']['english'])
		
		# Add character names
		prompt_parts.extend([c['name']['full'] for c in data['characters']['nodes']])
		
		# Join into comma-separated string and deduplicate
		full_prompt = ", ".join(list(dict.fromkeys(prompt_parts)))
		
		# Truncate to limit (ensuring it ends at a clean comma if possible)
		if len(full_prompt) > limit:
			truncated = full_prompt[:limit]
			return truncated[:truncated.rfind(',')]
		
		return full_prompt
		
	except Exception as e:
		logger.debug(f"Error fetching anime context: {e}")
		return ""

