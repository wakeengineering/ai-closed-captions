def _check_speech_with_silero(audio_chunk: 'np.ndarray', sr: int = 16000, threshold: float = 0.5) -> tuple[bool, float]:
	"""Check if audio chunk contains speech using Silero VAD.
	
	Args:
		audio_chunk: Audio samples as numpy array (float32, mono)
		sr: Sample rate (must be 16000 for Silero)
		threshold: Speech probability threshold (0.0-1.0)
	
	Returns:
		Tuple of (has_speech: bool, max_speech_prob: float)
	"""
	global _silero_vad_model, _silero_get_speech_timestamps
	
	# Lazy load Silero VAD model
	if _silero_vad_model is None:
		logger.debug("Loading Silero VAD model...")
		_silero_vad_model, utils = torch.hub.load(
			repo_or_dir='snakers4/silero-vad',
			model='silero_vad',
			force_reload=False,
			trust_repo=True
		)
		_silero_get_speech_timestamps = utils[0]  # get_speech_timestamps function
		logger.debug("Silero VAD model loaded")
	
	# Convert numpy to torch tensor
	audio_tensor = torch.from_numpy(audio_chunk).float()
	
	# Get speech timestamps (returns list of {start, end} dicts)
	_silero_vad_model.reset_states()
	speech_timestamps = _silero_get_speech_timestamps(
		audio_tensor,
		_silero_vad_model,
		sampling_rate=sr,
		threshold=threshold,
		min_speech_duration_ms=100,
		min_silence_duration_ms=100,
	)
	
	has_speech = len(speech_timestamps) > 0
	
	# Calculate speech ratio as proxy for confidence (total speech duration / total duration)
	if has_speech:
		total_speech_samples = sum(ts['end'] - ts['start'] for ts in speech_timestamps)
		speech_ratio = total_speech_samples / len(audio_chunk)
	else:
		speech_ratio = 0.0
	
	return has_speech, speech_ratio


def _get_audio_streams(video_path: Path) -> dict:
	"""Query video file for audio streams using ffprobe.
	
	Returns dict with audio stream count as key:
	{0: {'language': 'eng'}, 1: {'language': 'jpn'}}
	"""
	try:
		result = subprocess.run(
			[
				"ffprobe", "-v", "quiet",
				"-print_format", "json",
				"-show_streams",
				str(video_path)
			],
			capture_output=True,
			text=True,
			timeout=10
		)
		if result.returncode != 0:
			logger.debug(f"ffprobe error: {result.stderr}")
			return {}
		
		data = json.loads(result.stdout)
		audio_streams = {}
		audio_count = 0
		for stream in data.get('streams', []):
			if stream.get('codec_type') == 'audio':
				language = stream.get('tags', {}).get('language', 'unknown')
				audio_streams[audio_count] = {'language': language}
				audio_count += 1
		
		return audio_streams
	
	except Exception as e:
		logger.debug(f"Error querying audio streams: {e}")
		return {}

def _extract_audio_from_video(video_path: Path, output_audio_path: Path) -> bool:
	"""Extract English audio from video file, or first audio track if no English.
	
	Returns True if successful, False otherwise.
	"""
	audio_streams = _get_audio_streams(video_path)
	
	if not audio_streams:
		logger.basic(f"-- No audio streams found in {video_path.name}")
		return False
	
	# Look for English audio stream
	english_stream_idx = None
	for stream_idx, info in audio_streams.items():
		if info['language'].lower().startswith('en'):
			english_stream_idx = stream_idx
			break
	
	# Fallback to first audio stream
	if english_stream_idx is None:
		english_stream_idx = min(audio_streams.keys())
		logger.basic(f"-- No English audio found, using first audio track (index {english_stream_idx})")
	
	logger.basic(f"-- Extracting audio stream {english_stream_idx} from {video_path.name}")
	
	try:
		result = subprocess.run(
			[
				"ffmpeg", "-i", str(video_path),
				"-map", f"0:a:{english_stream_idx}?",
				"-vn", "-c:a", "pcm_s16le",
				"-ar", "16000", "-ac", "1",
				"-y",  # Overwrite output
				str(output_audio_path)
			],
			capture_output=True,
			text=True,
			timeout=600  # 10 minute timeout
		)
		
		if result.returncode != 0:
			logger.debug(f"ffmpeg error: {result.stderr}")
			return False
		
		if output_audio_path.exists():
			logger.debug(f"Audio extracted: {output_audio_path}")
			return True
		else:
			logger.basic(f"-- Audio extraction failed for {video_path.name}")
			return False
	
	except subprocess.TimeoutExpired:
		logger.basic(f"-- Audio extraction timeout for {video_path.name}")
		return False
	except Exception as e:
		logger.debug(f"Error extracting audio: {e}")
		return False

def _get_context(args) -> str:
	"""Extract context from args using priority: gemini > anilist."""
	initial_context = ""
	context_providers = [
		("Gemini", args.gemini_context, get_gemini_context),
		("AniList", args.http_context, get_http_context),
	]
	
	# Try each context provider in order, with fallback
	for idx, (provider_name, show_name, fetch_func) in enumerate(context_providers):
		if not show_name:
			continue
			
		logger.basic(f"-- Fetching context for: {show_name} ({provider_name}) --")
		initial_context = fetch_func(show_name)
		
		if initial_context:
			logger.basic(f"-- {provider_name} context loaded: {len(initial_context)} chars --")
			logger.debug(f"Context: {initial_context}")
			logger.debug(timer.checkpoint("Context fetching complete"))

			break
		elif idx < len(context_providers) - 1 and context_providers[idx + 1][1]:
			logger.basic(f"-- No {provider_name} context loaded, trying next provider --")
		else:
			logger.basic(f"-- No {provider_name} context loaded (API error or not found) --")
	return initial_context

